package net.codejava.spring.bean;

public class SbsEntEval {

	
	private String entidad;
	private String calificacion;
	private String deudaDirecta;
	private String vigente;
	private String reestruct;
	private String refinanc;
	private String vencida;
	private String judicial;
	
	
	
	public String getEntidad() {
		return entidad;
	}
	public void setEntidad(String entidad) {
		this.entidad = entidad;
	}
	public String getDeudaDirecta() {
		return deudaDirecta;
	}
	public void setDeudaDirecta(String deudaDirecta) {
		this.deudaDirecta = deudaDirecta;
	}
	public String getVigente() {
		return vigente;
	}
	public void setVigente(String vigente) {
		this.vigente = vigente;
	}
	public String getReestruct() {
		return reestruct;
	}
	public void setReestruct(String reestruct) {
		this.reestruct = reestruct;
	}
	public String getRefinanc() {
		return refinanc;
	}
	public void setRefinanc(String refinanc) {
		this.refinanc = refinanc;
	}
	public String getVencida() {
		return vencida;
	}
	public void setVencida(String vencida) {
		this.vencida = vencida;
	}
	public String getJudicial() {
		return judicial;
	}
	public void setJudicial(String judicial) {
		this.judicial = judicial;
	}
	public String getCalificacion() {
		return calificacion;
	}
	public void setCalificacion(String calificacion) {
		this.calificacion = calificacion;
	}


	
	
}
