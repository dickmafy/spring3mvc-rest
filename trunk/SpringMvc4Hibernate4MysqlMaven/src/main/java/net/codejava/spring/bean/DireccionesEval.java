package net.codejava.spring.bean;

public class DireccionesEval {

	private String fechaDireccion;
	private String direccion;
	private String distrito;
	private String provincia;
	private String departamento;
	private String fuente;
	
	
	
	public String getFechaDireccion() {
		return fechaDireccion;
	}
	public void setFechaDireccion(String fechaDireccion) {
		this.fechaDireccion = fechaDireccion;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getDistrito() {
		return distrito;
	}
	public void setDistrito(String distrito) {
		this.distrito = distrito;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public String getDepartamento() {
		return departamento;
	}
	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}
	public String getFuente() {
		return fuente;
	}
	public void setFuente(String fuente) {
		this.fuente = fuente;
	}
	
	

}
