package net.codejava.spring.wrapper;

public class LabelValueTo {

	private String label;
	private String value;

	public LabelValueTo(String value, String label) {
		super();
		this.value = value;
		this.label = label;
	}

	public String getLabel() {
		return label;
	}

	public String getValue() {
		return value;
	}

}
